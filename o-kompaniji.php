<script>
    document.addEventListener("DOMContentLoaded", function() {
        const sections = document.querySelectorAll('.gird_item');
        sections.forEach(section => {
            if (!section.classList.contains('okompaniji')) {
                section.style.display = 'none';
            }
        });

        
        const buttons = document.querySelectorAll('.blog_messonry_button button');
        buttons.forEach(button => {
            button.addEventListener('click', function() {
                const filter = this.getAttribute('data-filter');
                sections.forEach(section => {
                    if (section.classList.contains(filter.replace('.', ''))) {
                        section.style.display = 'block'; 
                    } else {
                        section.style.display = 'none'; 
                    }
                });
            });
        });
    });
</script>



